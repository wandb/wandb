"""
Example generation strategies for tests that rely on `hypothesis`.
"""

from __future__ import annotations

import re
from re import Pattern
from string import printable
from typing import Any, Literal

from hypothesis.strategies import (
    DrawFn,
    SearchStrategy,
    booleans,
    composite,
    deferred,
    fixed_dictionaries,
    floats,
    from_regex,
    integers,
    lists,
    one_of,
    text,
)
from wandb.sdk.automations import filters
from wandb.sdk.automations._filters.filter_ops import Scalar

FIELD_NAME_REGEX: Pattern[str] = re.compile(
    r"""
    \A   # String start, multiline not allowed
    \D   # field names cannot *start* with digits
    \w+  # [a-zA-Z0-9_]+ in ASCII mode
    \Z   # String end, multiline not allowed
    """,
    flags=re.VERBOSE | re.ASCII,
)


field_names: SearchStrategy[str] = from_regex(FIELD_NAME_REGEX)
"""Strategy to generate valid names for single (i.e. excluding nested) field or key names."""


@composite
def field_paths(
    draw: DrawFn, names: SearchStrategy[str] = field_names
) -> SearchStrategy[str]:
    """Strategy to generate strings that may represent single field names or nested paths of such fields.

    For strings representing nested fields, the successive field names are joined by dots.

    Possible examples generated by this strategy:
    - "my_field"
    - "myField.weights_and_biases"
    - "myField.weights_and_biases.my_subfield1"
    """

    # e.g. draw ("weights", "and_biases") -> return "weights.and_biases"
    path_parts = draw(lists(names, min_size=1, max_size=3))
    return ".".join(path_parts)


def finite_floats(width: Literal[16, 32, 64] = 32) -> SearchStrategy[float]:
    return floats(
        width=width, allow_nan=False, allow_infinity=False, allow_subnormal=False
    )


def printable_text(max_size: int = 100) -> SearchStrategy[str]:
    return text(printable, max_size=max_size)


scalars: SearchStrategy[Scalar] = one_of(
    integers(), finite_floats(), printable_text(), booleans()
)


def expr_dicts() -> SearchStrategy[dict[str, Any]]:
    return one_of(COMPARISON_OP_DICTS, LOGICAL_OP_DICTS, EVALUATION_OP_DICTS)


COMPARISON_OP_DICTS: SearchStrategy[dict[str, Any]] = deferred(
    lambda: one_of(
        fixed_dictionaries({filters.Gt.OP: scalars}),
        fixed_dictionaries({filters.Lt.OP: scalars}),
        fixed_dictionaries({filters.Gte.OP: scalars}),
        fixed_dictionaries({filters.Lte.OP: scalars}),
        fixed_dictionaries({filters.Eq.OP: scalars}),
        fixed_dictionaries({filters.Ne.OP: scalars}),
        fixed_dictionaries({filters.NotIn.OP: lists(scalars)}),
        fixed_dictionaries({filters.In.OP: lists(scalars)}),
    )
)
LOGICAL_OP_DICTS: SearchStrategy[dict[str, Any]] = deferred(  # Prevents RecursionError
    lambda: one_of(
        fixed_dictionaries({filters.Not.OP: expr_dicts()}),
        fixed_dictionaries({filters.And.OP: lists(expr_dicts())}),
        fixed_dictionaries({filters.Or.OP: lists(expr_dicts())}),
        fixed_dictionaries({filters.Nor.OP: lists(expr_dicts())}),
    )
)
EVALUATION_OP_DICTS: SearchStrategy[dict[str, Any]] = deferred(
    lambda: fixed_dictionaries({filters.Regex.OP: text()}),
)
